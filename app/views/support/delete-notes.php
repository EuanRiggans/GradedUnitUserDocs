<h2 class="section-heading">How to delete notes</h2>
<br/>
<h4 class="section-heading">Prerequisites</h4>
<p>In order to delete notes you will need the following:</p>
<ul>
    <li>You need to be <a href="https://<?php echo $_SERVER['SERVER_NAME'] . '/support/login/view'; ?>">logged in</a> to the
        skill tracker
    </li>
    <li>You need to have <a href="https://<?php echo $_SERVER['SERVER_NAME'] . '/support/selectsquad/view'; ?>">selected a
            squad.</a>
    </li>
    <li>You need to have <a href="https://<?php echo $_SERVER['SERVER_NAME'] . '/support/viewplayerskills/view'; ?>">selected a
            player</a> in a squad
    </li>
    <li>You need to be <a href="https://<?php echo $_SERVER['SERVER_NAME'] . '/support/viewplayernotes/view'; ?>">viewing a players notes</a>
    </li>
</ul>
<br/>
<h4 class="section-heading">How to delete notes</h4>
<p>To start you should be on the notes screen.</p>
<p>It should look like this: </p>
<a href="#">
    <img class="center" src="../../../static/img/deletenotes/noteshome.PNG" alt="">
</a>
<p>You can now select the note you wish to delete and then click the 'Delete Selected' button: </p>
<a href="#">
    <img class="center" src="../../../static/img/deletenotes/selectnote.PNG" alt="">
</a>
<p>You will be asked to confirm you wish to delete the note. When you are sure click the 'OK' button: </p>
<a href="#">
    <img class="center" src="../../../static/img/deletenotes/confirmation.PNG" alt="">
</a>
<p>The note will now be deleted and the notes list should be updated: </p>
<a href="#">
    <img class="center" src="../../../static/img/deletenotes/updatednotes.PNG" alt="">
</a>