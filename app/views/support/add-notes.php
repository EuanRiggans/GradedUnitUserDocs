<h2 class="section-heading">How to add new notes</h2>
<br/>
<h4 class="section-heading">Prerequisites</h4>
<p>In order to add new notes you will need the following:</p>
<ul>
    <li>You need to be <a href="https://<?php echo $_SERVER['SERVER_NAME'] . '/support/login/view'; ?>">logged in</a> to the
        skill tracker
    </li>
    <li>You need to have <a href="https://<?php echo $_SERVER['SERVER_NAME'] . '/support/selectsquad/view'; ?>">selected a
            squad.</a>
    </li>
    <li>You need to have <a href="https://<?php echo $_SERVER['SERVER_NAME'] . '/support/viewplayerskills/view'; ?>">selected a
            player</a> in a squad
    </li>
    <li>You need to be <a href="https://<?php echo $_SERVER['SERVER_NAME'] . '/support/viewplayernotes/view'; ?>">viewing a players notes</a>
    </li>
</ul>
<br/>
<h4 class="section-heading">How to add notes</h4>
<p>To start you should be on the notes screen.</p>
<p>It should look like this: </p>
<a href="#">
    <img class="center" src="../../../static/img/addnotes/noteshome.PNG" alt="">
</a>
<p>You can click the 'Add Notes' button and you will be asked for the note you wish to add: </p>
<a href="#">
    <img class="center" src="../../../static/img/addnotes/newnoteinput.PNG" alt="">
</a>
<p>Click the 'OK' button and the new note will be added.</p>
