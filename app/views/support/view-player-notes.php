<h2 class="section-heading">How to view a players notes</h2>
<br/>
<h4 class="section-heading">Prerequisites</h4>
<p>In order to view a players notes you will need the following:</p>
<ul>
    <li>You need to be <a href="https://<?php echo $_SERVER['SERVER_NAME'] . '/support/login/view'; ?>">logged in</a> to the
        skill tracker
    </li>
    <li>You need to have <a href="https://<?php echo $_SERVER['SERVER_NAME'] . '/support/selectsquad/view'; ?>">selected a
            squad.</a>
    </li>
    <li>You need to have <a href="https://<?php echo $_SERVER['SERVER_NAME'] . '/support/viewplayerskills/view'; ?>">selected a
            player</a> in a squad
    </li>
</ul>
<br/>
<h4 class="section-heading">How to view a players notes</h4>
<p>To view a players skill notes, firstly you should have selected a player.</p>
<p>It should look like this: </p>
<a href="#">
    <img class="center" src="../../../static/img/viewplayernotes/selectedplayer.PNG" alt="">
</a>
<p>You can now click on the drop down box 'Skill Categories' and then select the category you wish to view the notes for: </p>
<a href="#">
    <img class="center" src="../../../static/img/viewplayernotes/selectedskillcategory.png" alt="">
</a>
<p>Once you have selected a skill category, you can click the 'View Notes' button. And you will be taken to view the notes for that skill category.</p>
