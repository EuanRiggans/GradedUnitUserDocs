<h2 class="section-heading">How to view a players skills</h2>
<br/>
<h4 class="section-heading">Prerequisites</h4>
<p>In order to select a squad to manage will need the following:</p>
<ul>
    <li>You need to be <a href="https://<?php echo $_SERVER['SERVER_NAME'] . '/support/login'; ?>">logged in</a> to the
        skill tracker
    </li>
    <li>You need to have <a href="https://<?php echo $_SERVER['SERVER_NAME'] . '/support/selectsquad'; ?>">selected a
            squad.</a>
    </li>
    <li>You need to have <a href="https://<?php echo $_SERVER['SERVER_NAME'] . '/support/viewplayerskills'; ?>">selected a
            player</a> in a squad
    </li>
</ul>
<br/>
<h4 class="section-heading">How to view a players skills</h4>
<p>To view a players skill progress, firstly you should be on the <a href="https://<?php echo $_SERVER['SERVER_NAME'] . '/support/viewplayerskills'; ?>">skills view for a player</a>.</p>
<p>It should look like this: </p>
<a href="#">
    <img class="center" src="../../../static/img/viewskillprogress/skillshome.png" alt="">
</a>
<p>You can now click on the drop down box 'Skill categories' and a list of the players skills will appear. You can click on a skill category in that list to select it: </p>
<a href="#">
    <img class="center" src="../../../static/img/viewskillprogress/selectskill.png" alt="">
</a>
<p>You should now see a table which contains a list of skills within the selected skill category and their ratings.</p>
