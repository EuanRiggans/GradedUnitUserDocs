<h2 class="section-heading">How to view a edit skill progress</h2>
<br/>
<h4 class="section-heading">Prerequisites</h4>
<p>In order to edit a skill progress you will need the following:</p>
<ul>
    <li>You need to be <a href="https://<?php echo $_SERVER['SERVER_NAME'] . '/support/login/view'; ?>">logged in</a> to the
        skill tracker
    </li>
    <li>You need to have <a href="https://<?php echo $_SERVER['SERVER_NAME'] . '/support/selectsquad/view'; ?>">selected a
            squad.</a>
    </li>
    <li>You need to have <a href="https://<?php echo $_SERVER['SERVER_NAME'] . '/support/viewplayerskills/view'; ?>">selected a
            player</a> in a squad
    </li>
    <li>You need to be <a href="https://<?php echo $_SERVER['SERVER_NAME'] . '/support/viewskillprogress/view'; ?>">viewing the skill progress</a> of a player
    </li>
</ul>
<br/>
<h4 class="section-heading">How to edit a skill progress</h4>
<p>To start you should be on the skill details screen.</p>
<p>It should look like this: </p>
<a href="#">
    <img class="center" src="../../../static/img/editskillprogress/skillprogress.png" alt="">
</a>
<p>You can now double click on the skill rating column for the skill you wish to edit and you can input the new rating: </p>
<a href="#">
    <img class="center" src="../../../static/img/editskillprogress/inputnewrating.PNG" alt="">
</a>
<p>Once you have input the new rating, press enter and it will save the new rating.</p>
