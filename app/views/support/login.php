<h2 class="section-heading">How to login to simply rugby</h2>
<br/>
<h4 class="section-heading">Prerequisites</h4>
<p>In order to login to the Simply Rugby skill tracking system you will need the following:</p>
<ul>
    <li>A valid coach id</li>
    <li>A valid password</li>
</ul>
<p>If you have both of these then you are ready to login.</p>
<br/>
<h4 class="section-heading">How to login</h4>
<p>To login all you need to do is launch the Simply Rugby skill tracking software. Once it has launched you will need to
    type your coach id in the 'Member ID' box, and type your password into the 'Password' box. Remember that the
    password is case sensitive.</p>
<p>Once you have done this you can click the 'Login' button and you will be logged into the system.</p>
