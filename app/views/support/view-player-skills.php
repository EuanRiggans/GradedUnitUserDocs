<h2 class="section-heading">How to view a players skills</h2>
<br/>
<h4 class="section-heading">Prerequisites</h4>
<p>In order to select a squad to manage will need the following:</p>
<ul>
    <li>You need to be <a href="https://<?php echo $_SERVER['SERVER_NAME'] . '/support/login/view'; ?>">logged in</a> to the
        skill tracker
    </li>
    <li>You need to have <a href="https://<?php echo $_SERVER['SERVER_NAME'] . '/support/selectsquad/view'; ?>">selected a
            squad.</a>
    </li>
</ul>
<br/>
<h4 class="section-heading">How to view a players skills</h4>
<p>To view a players skills, firstly you should be on the home screen of the skill tracker after <a href="https://<?php echo $_SERVER['SERVER_NAME'] . '/support/selectsquad/view'; ?>">selecting a squad</a>.</p>
<p>It should look like this: </p>
<a href="#">
    <img class="center" src="../../../static/img/viewplayerskills/home.png" alt="">
</a>
<p>You can now click on the drop down box 'Squad players' and a list of the players in that squad will appear. You can click on a player in that list to select them: </p>
<a href="#">
    <img class="center" src="../../../static/img/viewplayerskills/selectingplayer.png" alt="">
</a>
<p>Once you have selected a player you can click the 'View Player Skill Data' button and you will be taken a page containing all of their skills.</p>